/**
 * ============================================================
 * 컴포넌트란?
 * ============================================================
 *
 * [저번 시간에 개발했던 웹페이지의 문제점]
 *
 * - 버튼을 클릭할 때마다 새로운 페이지로 이동함
 * - 매번 새로운 HTML 파일을 불러오면서 API를 다시 호출함
 * - 이로 인해 지연 시간이 발생함
 * - 여러 개의 HTML 파일에 단 한 줄의 코드만 다를 뿐, 나머지는 동일한 중복 코드가 반복해서 작성되었음
 *
 * ------------------------------------------------------------
 * 이번 시간의 목표
 * ------------------------------------------------------------
 *
 * 위와 같은 문제점들을 해결하기 위해 웹 개발에서 자주 사용되는 여러 가지 개념들을 학습한다.
 *
 * ------------------------------------------------------------
 * 컴포넌트 기반 개발
 * ------------------------------------------------------------
 *
 * 요즘 웹 애플리케이션은
 * - 구조가 복잡하고
 * - 규모가 크기 때문에
 * 기능 단위로 나누어 개발하는 방식이 일반적이다.
 * 이때 사용하는 단위가 바로 "컴포넌트"이다.
 *
 * ------------------------------------------------------------
 * 저번에 개발했던 웹 페이지의 기능 정리
 * ------------------------------------------------------------
 *
 * 1. 버튼에 적혀 있는 동물 이름에 따라 동물 사진 데이터를 API 호출로 가져오는 기능
 * 2. API 호출 결과를 바탕으로 알맞은 동물 사진을 화면에 표시하는 기능
 *
 * ------------------------------------------------------------
 * 화면 구조 분리
 * ------------------------------------------------------------
 *
 * 해당 웹 페이지는 다음 두 영역으로 나눌 수 있다.
 *
 * - tab-bar: 버튼들이 모여 있는 영역
 * - content: 동물 사진이 표시되는 영역
 *
 * 이처럼 기능 또는 역할별로 나눌 수 있는 화면의 부분을 컴포넌트라고 한다.
 *
 * ------------------------------------------------------------
 * 컴포넌트란?
 * ------------------------------------------------------------
 *
 * 웹 애플리케이션을 기능별로 나눈 독립적인 UI 구성 요소이다.
 *
 * ------------------------------------------------------------
 * 컴포넌트 기반 개발의 장점
 * ------------------------------------------------------------
 *
 * - 동일하거나 비슷한 화면을 중복된 코드를 작성하지 않고 재사용할 수 있다.
 *
 * - 웹 애플리케이션의 구조를 명확하게 분리할 수 있다.
 *
 * - 컴포넌트는 독립적인 요소이기 때문에 유지보수와 확장이 쉽다.
 *
 * ------------------------------------------------------------
 * .js 파일에서는 컴포넌트로 나누지 않았던 이유
 * ------------------------------------------------------------
 *
 * .js 파일에서는 코드가 겹친다고 해서 컴포넌트로 따로 나누지 않았다.
 * 그 이유는 컴포넌트는 주로 "사용자 인터페이스(UI)"와 "사용자와 상호작용하는 부분"을 나타내기 때문이다.
 * 즉, 컴포넌트는 UI 중심의 개념이다.
 *
 * ------------------------------------------------------------
 * 모듈(Module)이란?
 * ------------------------------------------------------------
 *
 * 모듈이란 재사용 가능한 코드의 집합으로, 특정 기능을 수행하는 코드를 하나의 단위로 묶어 관리하는 것이다.
 */
