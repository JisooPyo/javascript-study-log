/**
 * ============================================================
 * Node.js와 Express.js
 * ============================================================
 *
 * ------------------------------------------------------------
 * Node.js란?
 * ------------------------------------------------------------
 *
 * 크롬(Chrome) 브라우저의 V8 자바스크립트 엔진으로 빌드된 자바스크립트 실행 환경, 즉 런타임(Runtime) 환경.
 * 일반적으로 자바스크립트는 웹 브라우저 안에서만 실행되지만, Node.js를 사용하면 웹 브라우저 외부에서도 자바스크립트를 실행할 수 있다.
 *
 * 이를 통해 서버 개발, 파일 시스템 접근, 네트워크 통신 등 다양한 작업을 자바스크립트로 처리할 수 있다.
 *
 * ------------------------------------------------------------
 * Node.js만으로 서버를 구축할 때의 한계
 * ------------------------------------------------------------
 *
 * Node.js 자체만으로도 웹 서버를 구축할 수는 있지만,
 * 라우팅 처리, 요청/응답 관리, 미들웨어 구성
 * 같은 기능들을 모두 직접 구현해야 하기 때문에 복잡한 웹 서버를 만드는 과정이 번거롭고 시간이 많이 소요될 수 있다.
 *
 * ------------------------------------------------------------
 * Express.js란?
 * ------------------------------------------------------------
 *
 * Node.js 위에서 동작하는 웹 애플리케이션 프레임워크이다.
 * Express를 사용하면 웹 서버를 더 간단하게 구성할 수 있고 Node.js의 기능을 보다 효율적으로 활용할 수 있다.
 *
 * ------------------------------------------------------------
 * Express.js의 역할
 * ------------------------------------------------------------
 *
 * Express는
 * 라우팅, 미들웨어 처리, 요청(Request)과 응답(Response) 관리
 * 와 같은 서버 개발에 필요한 기능들을 간단하고 직관적인 방식으로 제공한다.
 *
 * 따라서 Node.js + Express.js 조합을 사용하면 복잡한 웹 서버를 훨씬 빠르고 편리하게 구축할 수 있다.
 */
