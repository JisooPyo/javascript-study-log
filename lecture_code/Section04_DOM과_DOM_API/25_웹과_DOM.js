/**
 * ============================================================
 * 웹과 DOM (Web & DOM)
 * ============================================================
 *
 * 자바스크립트는 웹페이지를 동적으로 만들어주기 위해 개발된 프로그래밍 언어이다.
 * 자바스크립트를 사용하면 웹페이지의 요소에 접근하고, 사용자의 행동에 따라 웹페이지를 동적으로 변경할 수 있다.
 *
 * 따라서 자바스크립트를 배우기 위해서는
 * "웹페이지가 어떻게 구성되어 있는지"와
 * "자바스크립트가 웹페이지에 어떻게 접근하는지"를 이해하는 것이 중요하다.
 */

/**
 * ------------------------------------------------------------
 * DOM (Document Object Model, 문서 객체 모델)
 * ------------------------------------------------------------
 *
 * 자바스크립트는 DOM을 통해 웹페이지의 요소에 접근할 수 있다.
 *
 * DOM이란,
 * HTML로 작성된 여러 요소들에 자바스크립트가 접근하고 조작할 수 있도록 브라우저가 변환해 놓은 객체 모델이다.
 *
 * 쉽게 말하면,
 * HTML로 작성된 요소들을 자바스크립트가 이해할 수 있도록 "객체 형태로 변환한 것"이 DOM이다.
 *
 * 자바스크립트는 이 DOM 객체를 통해
 * - HTML 요소를 선택하고
 * - 내용을 변경하고
 * - 스타일을 바꾸고
 * - 이벤트를 처리할 수 있다.
 */

/**
 * ------------------------------------------------------------
 * 웹을 이루는 구성 요소
 * ------------------------------------------------------------
 *
 * 웹(Web):
 * - 인터넷에서 사용자들이 정보를 공유할 수 있는 공간
 *
 * 웹페이지(Web Page):
 * - 웹에서 HTML로 작성된 하나의 페이지
 *
 * 웹사이트(Web Site):
 * - 여러 개의 웹페이지들의 집합
 * - URL(ex. https://www.example.com)에 접속해서 볼 수 있는 웹페이지들을 통틀어 부르는 말
 * - 흔히 "홈페이지"라고도 부름
 *
 * 웹사이트는 보통 다음 기술들로 구성되어 있다.
 */

/**
 * ------------------------------------------------------------
 * 웹을 구성하는 기술 요소
 * ------------------------------------------------------------
 *
 * HTML (Hyper Text Markup Language):
 * - 웹 페이지의 구조를 정의하는 역할
 * - 어떤 요소가 있고, 어떻게 구성되어 있는지를 나타냄
 *
 * CSS (Cascading Style Sheets):
 * - 웹 페이지의 요소들을 꾸며주는 역할
 * - 색상, 크기, 위치, 레이아웃 등을 담당
 *
 * JavaScript:
 * - 웹 페이지를 동적으로 만들어주는 역할
 * - 사용자와의 상호작용을 가능하게 함
 * - DOM을 조작하여 화면을 변경함
 */

/**
 * ------------------------------------------------------------
 * DOM 트리 (DOM Tree)
 * ------------------------------------------------------------
 *
 * 웹 브라우저는 HTML 문서를 불러온 다음, HTML 요소들 간의 상하 관계를 한눈에 볼 수 있도록 트리(Tree) 구조로 표현한다.
 * 이 구조를 DOM 트리(DOM Tree)라고 한다.
 *
 * 자바스크립트는 이 DOM 트리를 기반으로 특정 요소를 찾고, 수정하고, 삭제하거나 추가할 수 있다.
 */
